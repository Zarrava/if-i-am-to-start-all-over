user_input = input("Please enter a sentence:").strip() #this takes the input and takes out the leading and trailing spaces from the user input

print(f"Your input in lowercase: {user_input.lower()}") #this returns the user input in lowercase
print(f"Your input in title case: {user_input.title()}") #this returns the user input in title case
print(f"Your input in uppercase: {user_input.upper()}") #this returns the user input in uppercase